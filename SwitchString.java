class SwitchString
{
	public static void main(String[] args)
	{
		String choice = "two";
		switch(choice)
		{
			case "one":
				System.out.println(" Choice is one ");
				break;
			case "two":
				System.out.println(" Choice is two ");
				break;
			case "three":
				System.out.println(" Choice is three ");
				break;
			case "four":
				System.out.println(" Choice is four ");
				break;
		}
	}
}
